/****************************************************************************
* 名    称：WR_CMD()
* 功    能：发送命令
* 入口参数：cmd 命令编码
* 出口参数：无
****************************************************************************/         
void WR_CMD(char cmd)
{
	 int i;
	 //IO0CLR =WR;
	 IO0CLR =A0;
	  
	 for(i=7;i>=0;i--)
	 {
	   if( cmd&(1<<i) )
	   IO0SET=SI;
	   else
	   IO0CLR=SI;
	   IO0CLR=SCL;
	   IO0SET=SCL;  
	   //DelayNS(10);
	 } 
	 
	 //  IO0SET =WR;

}

