/****************************************************************************
* 名    称：GetKeyValue()
* 功    能：读取当前哪个按键被按下了
* 入口参数：无
* 出口参数：int - 返回当前按键按下对应的值
****************************************************************************/
int GetKeyValue(void)
{
	int key_value = KEY_NONE;
	if((IO1PIN & KEY_LINE1) == 0) 			{key_value = KEY_VALUE1;	while((IO1PIN & KEY_LINE1) == 0);}
	else if((IO1PIN & KEY_LINE2) == 0)   	{key_value = KEY_VALUE2; 	while((IO1PIN & KEY_LINE2) == 0);}
	else if((IO1PIN & KEY_LINE3) == 0)   	{key_value = KEY_VALUE3; 	while((IO1PIN & KEY_LINE3) == 0);}
	else if((IO1PIN & KEY_ENTER) == 0)  	{key_value = KEY_VALUE4;	while((IO1PIN & KEY_ENTER) == 0);}
	else if((IO1PIN & KEY_EXIT) == 0)    	{key_value = KEY_VALUE5;	while((IO1PIN & KEY_EXIT) == 0); }
	return key_value;
}

