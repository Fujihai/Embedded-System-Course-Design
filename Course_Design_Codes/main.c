/****************************************************************************
* 名    称：Time0Init()
* 功    能：初始化定时器
* 入口参数：无
* 出口参数：无
****************************************************************************/ 
void  Time0Init(void)
{  
   /* Fcclk = Fosc*4 = 11.0592MHz*4 = 44.2368MHz
	  Fpclk = Fcclk/4 = 44.2368MHz/4 = 11.0592MHz
	*/
	
	T0PR = 99;			    	    		// 设置定时器0分频为100分频，得1MHz
	T0MCR = 0x03;		   					// 匹配通道0匹配中断并复位T0TC
	T0MR0 =35000;	    					// 比较值(1S定时值)
	T0TCR = 0x03;		   					// 启动并复位T0TC
	T0TCR = 0x01; 
	
	/* 设置定时器0中断IRQ */
	VICIntSelect = 0x00;					// 所有中断通道设置为IRQ中断
	VICVectCntl0 = 0x24;					// 定时器0中断通道分配最高优先级(向量控制器0)
	VICVectAddr0 = (uint32)IRQ_Time0; 		// 设置中断服务程序地址向量 
	VICIntEnable = 0x00000010;				// 使能定时器0中断
}

/****************************************************************************
* 名    称：Time1Init()
* 功    能：初始化定时器1，定时时间为1S，并使能中断。 
* 入口参数：无
* 出口参数：无
****************************************************************************/
void Time1Init(void)
{   /* Fcclk = Fosc*4 = 11.0592MHz*4 = 44.2368MHz
	   Fpclk = Fcclk/4 = 44.2368MHz/4 = 11.0592MHz
	*/
	
	T1PR = 99;			    	    		// 设置定时器0分频为100分频，得110592Hz
	T1MCR = (3 << 3);		   			    // 匹配通道0匹配中断并复位T0TC
	T1MR1 = 10832;                           //比较值(1s定时值)
	T1TCR = 0x03;		   					// 启动并复位T1TC
	T1TCR = 0x01;
	 
	
	/* 设置定时器1中断IRQ */
	VICIntSelect = 0x00;					// 所有中断通道设置为IRQ中断
	VICVectCntl1 = 0x20 | 5;				// 定时器1中断通道分配向量地址1寄存器
	VICVectAddr1 = (uint32)IRQ_Time1; 		// 设置中断服务程序地址向量 
	VICIntEnable |= (1 << 5) ;				// 使能定时器1中断(定时器1的VIC通道号为5)
}



